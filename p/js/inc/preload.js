function preload(data){
	
	var filesHolder = $("<filesHolder>");
	
	$(data.images).each(function(i, filepath){
		var img = $("<img src='" + filepath + "' />");
		filesHolder.append(img);
	})
	
	$("body").append(filesHolder);
	
}



