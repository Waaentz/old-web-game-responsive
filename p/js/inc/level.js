function levelObject(data){
	
	var num 			= data.num ? data.num : 0;
	var name 			= data.name ? data.name : "Unnamed level";
	var difficulty 		= data.difficulty ? data.difficulty : 0;
	var startPosition 	= data.startPosition ? data.startPosition : [0,0];
	var map 			= data.map ? data.map : null;
	
	return {
		num:			num,
		name:			name,
		difficulty:		difficulty,
		startPosition:	startPosition,
		map:			map
	}
}